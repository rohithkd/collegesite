            <h4 class="deptcont line">Aim</h4><br>
            <p class="dept-style">The Electronics Engineering Department of Model Engineering College offers premier professional training with an eye on the modern developments in electronics and information technology. It offers a four year undergraduate course in electronics and communication engineering. The Electronics department is home to Digital Circuits, Communication, Industrial Instrumentation and Microprocessor Labs. </p>
            <p class="dept-style">Teamed with an excellent infrastructure, competitive faculty and an active students chapter of IEEE, the department which has received accreditation from NBA offers an outstanding opportunity for budding professionals to polish their skills and outperform their competitors. It provides a conducive environment that shapes students into professionals who remain in very high demand in the electronics industry. </p>
            <p class="dept-style">The curriculum is designed to give adequate emphasis on application of Electronics in Communication Systems and Signal Processing. It has also been a Microchip Consultant Program member since June 2005. Well equipped labs facilitated with the EDWIN Software and Virtual Modelling Software to carry out research projects. </p>
            <p class="dept-style">A syllabus encompassing the latest technologies, excellent lab facilities and an exemplary faculty are the main reasons for the prized status accorded to this course. The curriculum includes, apart from core electronics, papers on Communication Engineering, Instrumentation, Signal Processing, Process Control, ASCI design and Industrial Management. </p>

            <h4 class="deptcont line">Objectives</h4><br>
            <ul>
             <li class="dept-style">To provide a platform for students to realise, enhance, and polish their skills in the field of electronics and communication.</li>
             <li class="dept-style">To equip the engineers of tomorrow for the fast paced field of communication technology.</li>
             <li class="dept-style">To keep the students aware of the latest cutting edge technologies through talks, seminars etc.</li>
             <li class="dept-style">To reinforce into the students, through mini and main projects, the skills to work in a team.</li>
             <li class="dept-style">To establish, in each student, an uncompromising commitment to quality.</li>
            </ul>
