		<h4> Summer Internship Procedure </h4>
	
<li> The placement office contacts the companies for the summer internship.</li>

<li> The placement office sends in the interested students&rsquo; resumes (Company/Institute format) to the Companies.</li>
<li> The Companies send in the short list of students to the placement office.</li>
<li> A mutually convenient date is fixed for conducting the selection process. If the companies desire a SPT (Summer &nbsp;&nbsp;&nbsp;&nbsp;Placement Talk), the same can be arranged for prior to the recruitment process.</li>
<li> The interested companies conduct the summer internship recruitment process at MEC campus. After the selection &nbsp;&nbsp;&nbsp;&nbsp;process, companies announce the list of selected candidates on the campus itself.</li>
<li> Companies, unable to visit the campus can select the students on the basis of student resumes, telephonic &nbsp;&nbsp;&nbsp;&nbsp;interviews or/ and on-site interview.</li>

