            <h4 class="deptcont line">Aim</h4><br>
            <p class="dept-style">The department of Applied Sciences aims to provide a strong foundation in the fundamental sciences. Students of engineering are provided with the basic tools of analysis as well as the knowledge of the principles on which engineering is based. The department also strives to instill the scientific temper and the spirit of enquiry in students. To encourage students to understand and therefore apply the physical laws to the development of their fields of study. The department offers courses in Engineering mathematics , engineering physics and engineering chemistry within the engineering curricula. </p>
            
