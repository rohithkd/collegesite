	<h3>About</h3>
		<div class="box1">
			<p>The Model Engineering College Library has acquired a good collection of books and journals in the area of Electronics, Computer Science, Biomedical Engineering and related subjects like Science, Mathematics and Management.
			   The Library has approximately 16000 volumes of books and 60 journals. Books are classified according to Dewey Decimal classification scheme and are catalogued according to Anglo American Cataloguing rules II , with local variation.
			   The library provides information services such as current awareness service, selective dissemination of information service, reference service, bibliographic service etc. An amount of Rs50 lakhs has been sanctioned for modernisation of the library under TEQIP Project.
			</p>
		</div>

	<h3>Rules</h3>
		<div class="box1">
			<h4>Membership</h4>
			<p>Membership is restricted to students & staff of Model Engineering College.</p>
			<h5>Guidelines to Users</h5>
			<ul>
				<li>Working hours - Monday to Saturday 10. A M to 4.00 P M except on holidays, unless decided other wise by the Institute.</li>
				<li>Members/Users should sign in the register kept at the entrance before entering the library.</li>
				<li>Members / Users are not permitted to take personal belongings, personal copies of books or other printed materials / note books inside the library.</li>
				<li>Documents & library equipment's should be handled with care.</li>
				<li>Being an open- access library, users are advised not to replace the book to shelves after reference.</li>
				<li>Books will be reissued / renewed if there is no reservation. Only 2 consecutive renewals are permitted.</li>
				<li>Reference books & Journals will not be issued on loan.</li>
				<li>Photocopying service is available in the library. Document such as Seminar reports, Mini Project reports, Project reports will not be permitted for photocopying purpose.</li>
				<li>Borrower tickets are not transferable. Students should produce their Borrower tickets during registration to each semester.</li>
				<li>Loss of Borrowers tickets should be reported to the Librarian immediately. After detailed verification, if no book is issued against the same, a non- refundable fine of Rs. 50/- will be collected for each lost Borrowers tickets and the member has to execute an indemnity bond.</li>
				<li>Borrowers are responsible for the loss/ damage of books occurring during the loan period.</li>
				<li>Loss of books should be reported to the library immediately. It should be replaced with a new copy of the same / subsequent edition with in the due date. If the book is not available, 3 times the cost of books and fine up to the date on which the book is replaced are to be paid. If the book is out print, the replacement cost will be fixed according to the discretion of the librarian depending upon the importance of the book.</li>
			</ul>
			<h5>Clearance Certificate</h5>
			<ul>
				<li>All members are requested to produce a non- liability certificate to the office from the library, to the effect that they have returned all the books and cleared all the dues before obtaining hall - ticket for the final examination or issue of Transfer Certificate , whichever is earlier / leaving the institute.</li>
				<li>NLC will be issued only after clearing all the dues of the library and surrendering the Borrowers tickets issued from the library.</li>
			</ul>
		</div>

