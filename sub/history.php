			<h3>History</h3>
			
			<p>Model Engineering College was established in 1989 by the Institute of Human Resource Development to provide quality engineering education in the industrial centre of Kerala. 
				It was established as a self-financing college with a fair degree of autonomy. 
				The college started functioning in August 1989 in the premises of High School, Edapally with three degree courses offered in Biomedical, Computer and Electronics Engineering. 
				Prof. Sukulal was the first principal of the institute. The college was shifted to the present campus at Thrikkakara in September 1994 and has grown into an institution of excellence. 
				The design of the main building was inspired from India's first satellite, the Aryabhatta. 
			</p>
			<p>On its inception, Model Engineering College was the first self-financing college under the Government of Kerala and was the first college affiliated to CUSAT. 
				Currently, MEC is one of the most sought after Engineering colleges in Kerala. Our fame has spread throughout the country and students from different parts of the country have MEC as their preferred choice of college. 
				The college has been consistently ranked at the very top in various surveys of Private and Government Engineering colleges in Kerala, the MINT ranking being a good example. 
			</p>
			<p>Our students have consistently proven their mettle in varied areas and have won accolades from different quarters of the society. The passion for knowledge among students has resulted in the evolution of the college from a small unit into a prestigious institution with more than 100 faculty, 1500 students and 50 supporting staff. 
			</p>
			
			
			
